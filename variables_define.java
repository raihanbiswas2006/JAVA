public class variables_define {
    public static void main(String[] args) {

        int a = 10; // Integer (whole number)
        int b = 20;
        int sum = a + b; // Sum of a and b
        System.out.println(sum);
        int difference = a - b; // Difference of a and b
        System.out.println(difference);
        int mul = a * b; // Multiplication of a and b
        System.out.println(mul);
        int div = a / b; // Division of a and b
        System.out.println(div);
        int mod = a % b; // Modulus of a and b
        System.out.println(mod);
    }
}
